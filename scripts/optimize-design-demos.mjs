import { mkdir, writeFile } from 'node:fs/promises'
import path from 'node:path'

import {
  AxBootstrapFewShot,
  ai,
  ax,
} from '@ax-llm/ax'
import { compilePolicy, ready } from '@bitcoinerlab/miniscript-policies'

import { designTrainingSet } from './design-training-set.mjs'

const repoRoot = process.cwd()
const outputPath = path.join(repoRoot, 'src/data/optimizedDesignDemos.ts')
const reportPath = path.join(repoRoot, 'docs/design-optimization-report.json')
const executedTrainingSet = [
  designTrainingSet.find((example) => example.source === 'generated'),
  designTrainingSet.find((example) => example.source === 'sheet'),
].filter(Boolean)

function normalizePolicy(policy) {
  return policy.replace(/\s+/g, '')
}

function extractAtoms(policy) {
  return [
    ...policy.matchAll(
      /(pk\([^()]+\)|older\(\d+\)|after\(\d+\)|sha256\([^()]+\)|hash256\([^()]+\)|ripemd160\([^()]+\)|hash160\([^()]+\)|thresh\(\d+)/g,
    ),
  ].map((match) => match[0])
}

function scorePrediction(prediction, example) {
  if (!prediction || typeof prediction.policy !== 'string') {
    return 0
  }

  try {
    const compiled = compilePolicy(prediction.policy)
    if (!compiled.issane) {
      return 0
    }
  } catch {
    return 0
  }

  if (normalizePolicy(prediction.policy) === normalizePolicy(example.policy)) {
    return 1
  }

  const expectedAtoms = extractAtoms(example.policy)
  const predictedAtoms = new Set(extractAtoms(prediction.policy))
  const sharedAtoms = expectedAtoms.filter((atom) => predictedAtoms.has(atom))
  const atomCoverage =
    expectedAtoms.length > 0 ? sharedAtoms.length / expectedAtoms.length : 0

  if (atomCoverage >= 0.85) {
    return 0.8
  }
  if (atomCoverage >= 0.6) {
    return 0.6
  }

  return 0.25
}

await ready

const studentAI = ai({
  name: 'ollama',
  url: 'http://127.0.0.1:11434/v1',
  config: {
    model: 'qwen3:8b',
    temperature: 0.2,
    maxTokens: 220,
    stream: false,
  },
})

const designProgram = ax(
  'designBrief:string -> policy:string',
  {
    description:
      'Design Bitcoin Miniscript policies. Only use supported policy functions: pk, after, older, sha256, hash256, ripemd160, hash160, and, or, thresh. Return only a valid policy in the policy field.',
    maxRetries: 2,
  },
)

designProgram.setId('design')

const optimizer = new AxBootstrapFewShot({
  studentAI,
  options: {
    maxRounds: 1,
    maxExamples: 2,
    maxDemos: 2,
    batchSize: 1,
    verboseMode: false,
  },
})

console.log(
  `Optimizing with ${executedTrainingSet.length} mixed examples using local qwen3:8b`,
)

const result = await optimizer.compile(
  designProgram,
  executedTrainingSet.map((example) => ({
    designBrief: example.designBrief,
    policy: example.policy,
  })),
  ({ prediction, example }) => scorePrediction(prediction, example),
  { maxDemos: 2, maxIterations: 1 },
)

const expandedDemos = result.demos.map((demo) => ({
  ...demo,
  traces: demo.traces.map((trace) => {
    const matched = designTrainingSet.find(
      (example) => example.designBrief === trace.designBrief,
    )
    return matched ?? trace
  }),
}))

const banner = `// Generated by scripts/optimize-design-demos.mjs\n// Model: qwen3:8b via local Ollama\n// Executed examples: ${executedTrainingSet.length}\n// Full corpus available in scripts/design-training-set.mjs\n\n`
const source = `${banner}export const optimizedDesignDemos = ${JSON.stringify(expandedDemos, null, 2)}\n`

await writeFile(outputPath, source, 'utf8')
await mkdir(path.dirname(reportPath), { recursive: true })
await writeFile(
  reportPath,
  JSON.stringify(
    {
      model: 'qwen3:8b',
      trainingExamples: designTrainingSet.map(({ source, designBrief, policy }) => ({
        source,
        designBrief,
        policy,
      })),
      executedExamples: executedTrainingSet.map(
        ({ source, designBrief, policy }) => ({
          source,
          designBrief,
          policy,
        }),
      ),
      stats: result.stats,
      demos: expandedDemos,
    },
    null,
    2,
  ),
  'utf8',
)

console.log(`Wrote ${outputPath}`)
console.log(`Wrote ${reportPath}`)
